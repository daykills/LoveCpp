    int uniquePathsWithObstacles(vector<vector<int>>& obstacleGrid) {
        auto m = obstacleGrid.size();
        assert(m > 0);
        auto n = obstacleGrid[0].size();
        assert(n > 0);
        // initialize preRow
        vector<size_t> preRow(n, 0);
        for (auto col = 0; col < n; col++) {
            if (obstacleGrid[0][col] == 1)
                break;
            preRow[col] = 1;
        }
        // initialize preCol
        vector<size_t> preCol(m, 0);
        for (auto row = 0; row < m; row++) {
            if (obstacleGrid[row][0] == 1)
                break;
            preCol[row] = 1;
        }
        
        auto size = min(m, n);
        for (auto pre = 0; pre < size - 1; pre++) {
            // preRow/preCol at pre
            // Initialize [pre + 1][pre + 1]
            preRow[pre + 1] = preCol[pre + 1] =
                (obstacleGrid[pre + 1][pre + 1] == 1) ?
                0 : preRow[pre + 1] + preCol[pre + 1];
            // calculate curRow
            for (auto cur = pre + 2; cur < n; cur++) {
                preRow[cur] = (obstacleGrid[pre + 1][cur] == 1) ?
                    0 : preRow[cur - 1] + preRow[cur];
            }
            // calculate curCol
            for (auto cur = pre + 2; cur < m; cur++) {
                preCol[cur] = (obstacleGrid[cur][pre + 1] == 1) ?
                    0 : preCol[cur - 1] + preCol[cur];
            }
        }
        return m > n ? preCol[m - 1] : preRow[n - 1];
    }
